# 8. THE MAIN RESEARCH LOOP ENGINE
# ─────────────────────────────────────────────

class DeepResearchAgent:
    """
    The core research engine. Runs a relentless search loop that:
    
    1. Classifies task complexity
    2. Decomposes query into sub-queries
    3. Searches iteratively through source tiers
    4. Evaluates each result for quality/relevance
    5. Expands queries based on gaps found
    6. Cross-verifies findings across sources
    7. Only stops when confidence threshold met OR max rounds hit
    8. Synthesizes final answer with full source attribution
    
    Integrates with Cognithor's PGE Trinity as a specialized
    Planner sub-agent. The Gatekeeper checks the confidence
    score before passing to the Executor.
    """

    def __init__(
        self,
        search: SearchProvider,
        llm: LLMProvider,
        *,
        max_searches: int = 30,
        min_confidence: float = 0.75,
        fetch_full_pages: bool = True,
        progress_callback: Optional[Any] = None,  # async callable
    ):
        self.search = search
        self.llm = llm
        self.max_searches = max_searches
        self.min_confidence = min_confidence
        self.fetch_full_pages = fetch_full_pages
        self.progress_callback = progress_callback

        # Sub-components
        self.classifier = TaskComplexityClassifier(llm)
        self.decomposer = QueryDecomposer(llm)
        self.evaluator = ResultEvaluator(llm)
        self.expander = AdaptiveQueryExpander(llm)
        self.synthesizer = SolutionSynthesizer(llm)

    async def research(self, query: str) -> dict:
        """
        Main entry point. Returns a dict with:
        {
            "answer": str,           # The synthesized answer
            "confidence": float,     # Overall confidence 0.0-1.0
            "verified": bool,        # Whether solution is cross-verified
            "complexity": str,       # Task complexity classification
            "searches_performed": int,
            "sources_found": int,
            "elapsed_seconds": float,
            "findings": [...],       # Detailed findings
            "top_sources": [...]     # Best sources with URLs
        }
        """
        logger.info(f"Starting deep research: {query}")

        # ── Phase 1: Classify complexity ──
        complexity = await self.classifier.classify(query)
        await self._emit_progress(f"Task classified as: {complexity.name}")

        # Adjust parameters based on complexity
        plan = self._create_plan(query, complexity)

        # ── Phase 2: Decompose query ──
        plan.current_phase = ResearchPhase.QUERY_DECOMPOSITION
        sub_queries = await self.decomposer.decompose(query)
        plan.sub_queries = [sq["query"] for sq in sub_queries]
        await self._emit_progress(
            f"Research plan: {len(plan.sub_queries)} initial queries generated"
        )

        # ── Phase 3: THE RELENTLESS LOOP ──
        round_num = 0
        queries_to_run = list(plan.sub_queries)
        seen_urls: set[str] = set()

        while (
            plan.search_count < plan.max_search_rounds
            and not plan.has_verified_solution
            and queries_to_run
        ):
            round_num += 1
            plan.current_phase = self._determine_phase(round_num)

            await self._emit_progress(
                f"Round {round_num} | Phase: {plan.current_phase.name} | "
                f"Searches: {plan.search_count}/{plan.max_search_rounds} | "
                f"Best confidence: {plan.best_confidence:.0%}"
            )

            # Run current batch of queries
            batch = queries_to_run[:3]  # 3 queries per round
            queries_to_run = queries_to_run[3:]

            for q in batch:
                if plan.search_count >= plan.max_search_rounds:
                    break

                raw_results = await self.search.search(q, max_results=8)
                plan.search_count += 1

                for raw in raw_results:
                    url = raw.get("url", "")
                    if url in seen_urls:
                        continue
                    seen_urls.add(url)

                    result = SearchResult(
                        url=url,
                        title=raw.get("title", ""),
                        snippet=raw.get("snippet", ""),
                    )

                    # Fetch full page content for deeper analysis
                    if self.fetch_full_pages:
                        try:
                            result.full_content = await self.search.fetch_page(url)
                        except Exception as e:
                            logger.debug(f"Failed to fetch {url}: {e}")
                            result.full_content = result.snippet

                    # Evaluate quality
                    result = await self.evaluator.evaluate(query, result)
                    plan.all_results.append(result)

                    # If this looks promising, create/update a finding
                    if result.relevance_score >= 0.5:
                        self._update_findings(plan, result)

                    await self._emit_progress(
                        f"  [{result.source_tier.name}] "
                        f"Score: {result.relevance_score:.0%} | "
                        f"Working: {result.has_working_solution} | "
                        f"{result.title[:60]}"
                    )

            # ── Check: Do we have enough? ──
            if plan.has_verified_solution:
                await self._emit_progress(
                    "✅ HIGH-CONFIDENCE VERIFIED SOLUTION FOUND"
                )
                break

            # ── If not, generate new queries based on gaps ──
            if not queries_to_run and plan.search_count < plan.max_search_rounds:
                new_queries = await self.expander.expand(plan)
                if new_queries:
                    queries_to_run.extend(new_queries)
                    plan.sub_queries.extend(new_queries)
                    await self._emit_progress(
                        f"Expanding search: {len(new_queries)} new queries "
                        f"targeting identified gaps"
                    )
                else:
                    await self._emit_progress(
                        "No more expansion queries generated. Synthesizing results."
                    )
                    break

        # ── Phase 4: Cross-verification ──
        plan.current_phase = ResearchPhase.CROSS_VERIFICATION
        self._cross_verify_findings(plan)

        # ── Phase 5: Synthesize final answer ──
        plan.current_phase = ResearchPhase.SYNTHESIS
        answer = await self.synthesizer.synthesize(plan)

        # ── Build result ──
        plan.current_phase = ResearchPhase.DONE
        top_sources = sorted(
            plan.all_results,
            key=lambda r: r.relevance_score,
            reverse=True,
        )[:10]

        result = {
            "answer": answer,
            "confidence": plan.best_confidence,
            "verified": plan.has_verified_solution,
            "complexity": complexity.name,
            "searches_performed": plan.search_count,
            "sources_found": len(plan.all_results),
            "unique_urls": len(seen_urls),
            "elapsed_seconds": plan.elapsed_seconds,
            "findings": [
                {
                    "claim": f.claim,
                    "confidence": f.confidence,
                    "verified": f.is_verified,
                    "cross_verified": f.cross_verified,
                    "num_sources": len(f.supporting_sources),
                }
                for f in plan.findings
            ],
            "top_sources": [
                {
                    "url": s.url,
                    "title": s.title,
                    "tier": s.source_tier.name,
                    "score": s.relevance_score,
                    "has_solution": s.has_working_solution,
                }
                for s in top_sources
            ],
        }

        await self._emit_progress(
            f"Research complete: {plan.search_count} searches, "
            f"{len(plan.all_results)} sources, "
            f"confidence: {plan.best_confidence:.0%}, "
            f"time: {plan.elapsed_seconds:.1f}s"
        )

        return result

    # ── Helper methods ──

    def _create_plan(self, query: str, complexity: TaskComplexity) -> ResearchPlan:
        """Creates research plan with parameters tuned to complexity."""
        params = {
            TaskComplexity.ROUTINE:       (8,  0.8),
            TaskComplexity.MODERATE:       (15, 0.75),
            TaskComplexity.RESEARCH_HEAVY: (25, 0.65),
            TaskComplexity.FRONTIER:       (30, 0.55),
        }
        max_rounds, min_conf = params.get(complexity, (25, 0.7))

        return ResearchPlan(
            original_query=query,
            complexity=complexity,
            max_search_rounds=min(max_rounds, self.max_searches),
            min_confidence_threshold=max(min_conf, self.min_confidence),
        )

    def _determine_phase(self, round_num: int) -> ResearchPhase:
        """Maps round number to research phase."""
        if round_num <= 2:
            return ResearchPhase.OFFICIAL_SEARCH
        elif round_num <= 4:
            return ResearchPhase.GITHUB_DEEP_DIVE
        else:
            return ResearchPhase.COMMUNITY_MINING

    def _update_findings(self, plan: ResearchPlan, result: SearchResult) -> None:
        """Updates or creates findings based on a relevant result."""
        # Check if this result supports an existing finding
        merged = False
        for finding in plan.findings:
            if self._results_agree(finding, result):
                finding.supporting_sources.append(result)
                # Recalculate confidence based on source count and quality
                finding.confidence = self._calculate_finding_confidence(finding)
                if result.has_working_solution:
                    finding.is_verified = True
                merged = True
                break

        if not merged:
            # Create new finding
            finding = ResearchFinding(
                claim=self._extract_claim(result),
                confidence=result.relevance_score * 0.7,  # Single source penalty
                supporting_sources=[result],
                is_verified=result.has_working_solution,
            )
            plan.findings.append(finding)

    def _results_agree(self, finding: ResearchFinding, result: SearchResult) -> bool:
        """
        Checks if a new result agrees with an existing finding.
        Simple heuristic: if solution steps overlap significantly.
        """
        if not finding.supporting_sources:
            return False

        # Check for overlapping solution steps
        existing_steps = set()
        for src in finding.supporting_sources:
            existing_steps.update(s.lower() for s in src.solution_steps)

        new_steps = set(s.lower() for s in result.solution_steps)

        if existing_steps and new_steps:
            overlap = existing_steps & new_steps
            return len(overlap) >= min(1, len(new_steps) // 2)

        return False

    def _calculate_finding_confidence(self, finding: ResearchFinding) -> float:
        """
        Confidence formula:
        - Base: average relevance of supporting sources
        - Boost: +0.1 per additional independent source (capped at +0.3)
        - Boost: +0.15 if cross-verified (2+ domains)
        - Boost: +0.1 if any source is official docs
        - Penalty: -0.1 per contradicting source
        """
        if not finding.supporting_sources:
            return 0.0

        avg_relevance = sum(
            s.relevance_score for s in finding.supporting_sources
        ) / len(finding.supporting_sources)

        # Source count boost (diminishing returns)
        source_boost = min(0.3, (len(finding.supporting_sources) - 1) * 0.1)

        # Cross-verification boost
        cross_boost = 0.15 if finding.cross_verified else 0.0

        # Official source boost
        official_boost = 0.1 if any(
            s.source_tier == SourceTier.OFFICIAL_DOCS
            for s in finding.supporting_sources
        ) else 0.0

        # Contradiction penalty
        contra_penalty = len(finding.contradicting_sources) * 0.1

        confidence = (
            avg_relevance + source_boost + cross_boost + official_boost - contra_penalty
        )
        return max(0.0, min(1.0, confidence))

    def _extract_claim(self, result: SearchResult) -> str:
        """Extracts the core claim/solution from a search result."""
        if result.solution_steps:
            return f"Solution ({len(result.solution_steps)} steps): " + "; ".join(
                result.solution_steps[:3]
            )
        return result.snippet[:200]

    def _cross_verify_findings(self, plan: ResearchPlan) -> None:
        """Final cross-verification pass across all findings."""
        for finding in plan.findings:
            finding.confidence = self._calculate_finding_confidence(finding)
            if finding.cross_verified and finding.confidence >= plan.min_confidence_threshold:
                finding.is_verified = True

    async def _emit_progress(self, message: str) -> None:
        """Sends progress updates via callback."""
        logger.info(message)
        if self.progress_callback:
            try:
                await self.progress_callback(message)
            except Exception:
                pass


