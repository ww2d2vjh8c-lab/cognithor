# 4. QUERY DECOMPOSER
# ─────────────────────────────────────────────

DECOMPOSER_PROMPT = """You are an expert research query decomposer.

Given a technical query, break it down into a systematic research plan.
Generate search queries in ORDER OF PRIORITY:

1. OFFICIAL sources first (docs, compatibility matrices, release notes)
2. GITHUB sources (issues, PRs, discussions for relevant repos)
3. COMMUNITY sources (Reddit, forums, Stack Overflow, blog posts)
4. LATERAL sources (similar hardware/software that might have workarounds)

For each query, specify:
- The actual search string to use
- Which source tier it targets
- What information we're looking for

CRITICAL: Generate diverse queries. Don't just rephrase the same thing.
Think about what ADJACENT information might help solve the problem.

Example for "run vLLM on AMD 9070 XT":
- "vLLM ROCm RDNA 4 compatibility" → OFFICIAL (does vLLM officially support this?)
- "vllm-project/vllm AMD GPU" site:github.com → GITHUB (any issues/PRs?)
- "9070 XT ROCm version support" → OFFICIAL (which ROCm version works?)
- "vLLM AMD Radeon" reddit → COMMUNITY (has anyone tried this?)
- "7900 XTX vLLM setup guide" → LATERAL (what worked for similar AMD GPU?)
- "RDNA 4 gfx12 ROCm docker" → COMMUNITY (Docker-specific solutions?)
- "pytorch ROCm RDNA 4 compile" → LATERAL (prerequisite: does PyTorch work?)

Respond in JSON format:
{{
  "sub_queries": [
    {{"query": "...", "tier": "OFFICIAL|GITHUB|COMMUNITY|LATERAL", "looking_for": "..."}}
  ],
  "key_unknowns": ["what we don't know yet"],
  "related_technologies": ["tech stack components to investigate"]
}}

Original query: {query}"""


class QueryDecomposer:
    """Breaks down complex queries into systematic search plans."""

    def __init__(self, llm: LLMProvider):
        self.llm = llm

    async def decompose(self, query: str) -> list[dict]:
        response = await self.llm.complete(
            system_prompt="You are a research planning expert. Always respond in valid JSON.",
            user_prompt=DECOMPOSER_PROMPT.format(query=query),
            temperature=0.4,
        )

        try:
            # Strip markdown fences if present
            cleaned = response.strip()
            if cleaned.startswith("```"):
                cleaned = cleaned.split("\n", 1)[1]
            if cleaned.endswith("```"):
                cleaned = cleaned.rsplit("```", 1)[0]
            cleaned = cleaned.strip()

            data = json.loads(cleaned)
            return data.get("sub_queries", [])
        except json.JSONDecodeError:
            logger.warning("Failed to parse decomposition, using fallback")
            return [{"query": query, "tier": "COMMUNITY", "looking_for": "any solution"}]


