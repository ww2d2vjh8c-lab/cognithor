# 3. TASK COMPLEXITY CLASSIFIER
# ─────────────────────────────────────────────

COMPLEXITY_CLASSIFIER_PROMPT = """You are a task complexity classifier for technical support queries.

Analyze the query and classify it into exactly ONE category:

ROUTINE — Well-documented procedure. Official docs exist, widely done.
  Examples: "install nginx on Ubuntu", "create a Python venv"

MODERATE — Mostly documented but needs some specific configuration.
  Examples: "set up PostgreSQL replication", "configure CUDA for PyTorch"

RESEARCH_HEAVY — Bleeding-edge, experimental, or poorly documented.
  Signals: very new hardware/software, no official support, community workarounds only.
  Examples: "run vLLM on AMD RDNA 4", "use Whisper with RK3588 NPU"

FRONTIER — Almost nobody has successfully done this yet.
  Examples: "run llama.cpp on RISC-V with vector extensions"

Respond with ONLY the category name and a one-line justification.

Query: {query}"""


class TaskComplexityClassifier:
    """Classifies incoming tasks to determine research intensity."""

    def __init__(self, llm: LLMProvider):
        self.llm = llm

    async def classify(self, query: str) -> TaskComplexity:
        response = await self.llm.complete(
            system_prompt="You are a precise classifier. Respond only with the category name and one line of justification.",
            user_prompt=COMPLEXITY_CLASSIFIER_PROMPT.format(query=query),
            temperature=0.1,
        )

        response_upper = response.upper()
        if "FRONTIER" in response_upper:
            return TaskComplexity.FRONTIER
        elif "RESEARCH_HEAVY" in response_upper:
            return TaskComplexity.RESEARCH_HEAVY
        elif "MODERATE" in response_upper:
            return TaskComplexity.MODERATE
        else:
            return TaskComplexity.ROUTINE


