class SourceTier(Enum):
    """Source priority hierarchy — higher tier = more trustworthy."""
    OFFICIAL_DOCS = 5       # Official documentation, compatibility matrices
    GITHUB_ISSUES = 4       # Project issues, PRs, release notes
    VERIFIED_COMMUNITY = 3  # Reddit/forum posts with confirmed success
    BLOG_TUTORIAL = 2       # Blog posts, tutorials, guides
    UNVERIFIED = 1          # Random forum posts, no confirmation
    IRRELEVANT = 0          # Off-topic results


class ResearchPhase(Enum):
    """Tracks which phase the research loop is in."""
    QUERY_DECOMPOSITION = auto()
    OFFICIAL_SEARCH = auto()
    GITHUB_DEEP_DIVE = auto()
    COMMUNITY_MINING = auto()
    CROSS_VERIFICATION = auto()
    SYNTHESIS = auto()
    DONE = auto()


class TaskComplexity(Enum):
    """Classifier output for incoming tasks."""
    ROUTINE = auto()        # Well-documented, standard procedure
    MODERATE = auto()       # Some nuance, may need 3-5 searches
    RESEARCH_HEAVY = auto() # Bleeding edge, experimental, poor docs
    FRONTIER = auto()       # Basically nobody has done this yet


@dataclass
class SearchResult:
    """Single search result with metadata and quality scoring."""
    url: str
    title: str
    snippet: str
    full_content: str = ""
    source_tier: SourceTier = SourceTier.UNVERIFIED
    relevance_score: float = 0.0        # 0.0–1.0, LLM-judged
    has_working_solution: bool = False   # Did someone confirm it works?
    solution_steps: list[str] = field(default_factory=list)
    timestamp: str = ""                 # When was this posted?
    sha256: str = ""                    # Content hash for dedup

    def __post_init__(self):
        if self.full_content and not self.sha256:
            self.sha256 = hashlib.sha256(
                self.full_content.encode()
            ).hexdigest()[:16]


@dataclass
class ResearchFinding:
    """A synthesized finding backed by multiple sources."""
    claim: str
    confidence: float  # 0.0–1.0
    supporting_sources: list[SearchResult] = field(default_factory=list)
    contradicting_sources: list[SearchResult] = field(default_factory=list)
    is_verified: bool = False

    @property
    def cross_verified(self) -> bool:
        """A finding is cross-verified if 2+ independent sources agree."""
        unique_domains = set()
        for src in self.supporting_sources:
            domain = src.url.split("/")[2] if "/" in src.url else src.url
            unique_domains.add(domain)
        return len(unique_domains) >= 2


@dataclass
class ResearchPlan:
    """The research strategy generated by query decomposition."""
    original_query: str
    complexity: TaskComplexity
    sub_queries: list[str] = field(default_factory=list)
    search_strategies: list[str] = field(default_factory=list)
    max_search_rounds: int = 25
    min_confidence_threshold: float = 0.75
    current_phase: ResearchPhase = ResearchPhase.QUERY_DECOMPOSITION
    findings: list[ResearchFinding] = field(default_factory=list)
    all_results: list[SearchResult] = field(default_factory=list)
    search_count: int = 0
    started_at: float = field(default_factory=time.time)

    @property
    def best_confidence(self) -> float:
        if not self.findings:
            return 0.0
        return max(f.confidence for f in self.findings)

    @property
    def has_verified_solution(self) -> bool:
        return any(
            f.is_verified and f.confidence >= self.min_confidence_threshold
            for f in self.findings
        )

    @property
    def elapsed_seconds(self) -> float:
        return time.time() - self.started_at


# ─────────────────────────────────────────────
# 2. ABSTRACT INTERFACES (for Cognithor integration)
# ─────────────────────────────────────────────

class SearchProvider(ABC):
    """
    Abstract search interface — implement for your preferred
    search backend (SearXNG, Tavily, Brave, Perplexity API, etc.)
    """

    @abstractmethod
    async def search(self, query: str, max_results: int = 10) -> list[dict]:
        """Returns list of {url, title, snippet}."""
        ...

    @abstractmethod
    async def fetch_page(self, url: str) -> str:
        """Fetches full page content from URL."""
        ...


class LLMProvider(ABC):
    """
    Abstract LLM interface — implement for your preferred
    LLM (Ollama, OpenAI, Anthropic, local model, etc.)
    """

    @abstractmethod
    async def complete(
        self,
        system_prompt: str,
        user_prompt: str,
        temperature: float = 0.3,
    ) -> str:
        """Returns LLM completion text."""
        ...


