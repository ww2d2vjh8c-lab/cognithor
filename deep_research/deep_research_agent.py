# 6. ADAPTIVE QUERY EXPANDER
# ─────────────────────────────────────────────

EXPANDER_PROMPT = """You are a search query strategist.

The original research question is: "{original_query}"

So far, we've done {search_count} searches and our best confidence is {best_confidence:.0%}.

Here's what we've found so far:
{findings_summary}

Here's what we still DON'T know:
{gaps}

Generate 3-5 NEW search queries that:
1. Target the specific gaps we haven't filled
2. Use DIFFERENT keywords and angles than previous searches
3. Try lateral approaches (similar hardware, adjacent projects, workarounds)
4. Include site-specific searches where helpful (github.com, reddit.com)

Previous queries used (DO NOT repeat these):
{previous_queries}

Respond in JSON: {{"new_queries": ["query1", "query2", ...]}}"""


class AdaptiveQueryExpander:
    """Generates new search queries based on what's been found so far."""

    def __init__(self, llm: LLMProvider):
        self.llm = llm

    async def expand(self, plan: ResearchPlan) -> list[str]:
        findings_summary = "\n".join(
            f"- {f.claim} (confidence: {f.confidence:.0%})"
            for f in plan.findings
        ) or "Nothing conclusive yet."

        gaps = self._identify_gaps(plan)

        response = await self.llm.complete(
            system_prompt="You are a creative search strategist. Respond in valid JSON.",
            user_prompt=EXPANDER_PROMPT.format(
                original_query=plan.original_query,
                search_count=plan.search_count,
                best_confidence=plan.best_confidence,
                findings_summary=findings_summary,
                gaps=gaps,
                previous_queries=json.dumps(plan.sub_queries, indent=2),
            ),
            temperature=0.6,  # Higher creativity for diverse queries
        )

        try:
            cleaned = response.strip()
            if cleaned.startswith("```"):
                cleaned = cleaned.split("\n", 1)[1]
            if cleaned.endswith("```"):
                cleaned = cleaned.rsplit("```", 1)[0]
            data = json.loads(cleaned.strip())
            return data.get("new_queries", [])
        except json.JSONDecodeError:
            return []

    def _identify_gaps(self, plan: ResearchPlan) -> str:
        gaps = []
        has_official = any(
            r.source_tier == SourceTier.OFFICIAL_DOCS
            for r in plan.all_results
            if r.relevance_score > 0.5
        )
        has_verified = any(r.has_working_solution for r in plan.all_results)
        has_steps = any(r.solution_steps for r in plan.all_results)

        if not has_official:
            gaps.append("No official documentation found confirming support")
        if not has_verified:
            gaps.append("No verified working solution found from anyone")
        if not has_steps:
            gaps.append("No concrete step-by-step instructions found")
        if not gaps:
            gaps.append("Need cross-verification from additional sources")

        return "\n".join(f"- {g}" for g in gaps)


