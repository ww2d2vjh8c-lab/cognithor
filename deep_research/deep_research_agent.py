class GatekeeperResearchPolicy:
    """
    Policy for Cognithor's Gatekeeper to decide when to
    invoke the DeepResearchAgent vs. standard search.

    Usage in Cognithor's policy engine:
    
        gatekeeper = GatekeeperResearchPolicy(classifier)
        
        if await gatekeeper.needs_deep_research(task):
            result = await deep_research_agent.research(task.query)
            if gatekeeper.result_acceptable(result):
                executor.execute(result["answer"])
            else:
                executor.report_inconclusive(result)
    """

    def __init__(self, classifier: TaskComplexityClassifier):
        self.classifier = classifier

    async def needs_deep_research(self, query: str) -> bool:
        """Returns True if the query needs DeepResearch instead of standard search."""
        complexity = await self.classifier.classify(query)
        return complexity in (TaskComplexity.RESEARCH_HEAVY, TaskComplexity.FRONTIER)

    def result_acceptable(
        self,
        result: dict,
        *,
        min_confidence: float = 0.65,
    ) -> bool:
        """
        Gatekeeper check: Is the research result good enough
        to pass to the Executor for action?
        """
        return (
            result.get("confidence", 0) >= min_confidence
            and result.get("sources_found", 0) >= 2
        )


