# 5. RESULT EVALUATOR (the "Confidence Judge")
# ─────────────────────────────────────────────

EVALUATOR_PROMPT = """You are a strict technical solution evaluator.

Given a search result for the query "{query}", evaluate:

1. RELEVANCE (0.0-1.0): Does this directly address the query?
2. ACTIONABILITY (0.0-1.0): Does it contain concrete, executable steps?
3. VERIFICATION (0.0-1.0): Is there evidence this actually works?
   - 1.0 = Author confirms success with exact same setup
   - 0.7 = Author confirms success with similar setup
   - 0.4 = Theoretical/untested suggestion
   - 0.1 = Clearly outdated or different context
4. SOURCE_TIER: OFFICIAL_DOCS | GITHUB_ISSUES | VERIFIED_COMMUNITY | BLOG_TUTORIAL | UNVERIFIED
5. HAS_WORKING_SOLUTION: true/false — Does this contain a confirmed working solution?
6. SOLUTION_STEPS: If there are concrete steps, list them.

Respond in JSON:
{{
  "relevance": 0.0,
  "actionability": 0.0,
  "verification": 0.0,
  "source_tier": "...",
  "has_working_solution": false,
  "solution_steps": [],
  "reasoning": "one line explaining your assessment"
}}

Search result title: {title}
Search result URL: {url}
Content:
{content}"""


class ResultEvaluator:
    """LLM-powered judge that scores search results for quality."""

    def __init__(self, llm: LLMProvider):
        self.llm = llm

    async def evaluate(
        self, query: str, result: SearchResult
    ) -> SearchResult:
        """Evaluates and enriches a SearchResult with quality scores."""
        content_preview = result.full_content[:3000] or result.snippet

        response = await self.llm.complete(
            system_prompt="You are a strict evaluator. Respond only in valid JSON.",
            user_prompt=EVALUATOR_PROMPT.format(
                query=query,
                title=result.title,
                url=result.url,
                content=content_preview,
            ),
            temperature=0.1,
        )

        try:
            cleaned = response.strip()
            if cleaned.startswith("```"):
                cleaned = cleaned.split("\n", 1)[1]
            if cleaned.endswith("```"):
                cleaned = cleaned.rsplit("```", 1)[0]

            scores = json.loads(cleaned.strip())

            # Composite relevance score
            result.relevance_score = (
                scores.get("relevance", 0) * 0.3
                + scores.get("actionability", 0) * 0.3
                + scores.get("verification", 0) * 0.4  # Verification weighted highest
            )

            tier_map = {
                "OFFICIAL_DOCS": SourceTier.OFFICIAL_DOCS,
                "GITHUB_ISSUES": SourceTier.GITHUB_ISSUES,
                "VERIFIED_COMMUNITY": SourceTier.VERIFIED_COMMUNITY,
                "BLOG_TUTORIAL": SourceTier.BLOG_TUTORIAL,
                "UNVERIFIED": SourceTier.UNVERIFIED,
            }
            result.source_tier = tier_map.get(
                scores.get("source_tier", ""), SourceTier.UNVERIFIED
            )
            result.has_working_solution = scores.get("has_working_solution", False)
            result.solution_steps = scores.get("solution_steps", [])

        except (json.JSONDecodeError, KeyError) as e:
            logger.warning(f"Evaluation parse failed: {e}")
            result.relevance_score = 0.2

        return result


