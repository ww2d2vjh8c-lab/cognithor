# 7. SOLUTION SYNTHESIZER
# ─────────────────────────────────────────────

SYNTHESIZER_PROMPT = """You are a technical solution synthesizer.

Original question: {query}

Based on {num_sources} sources searched, here are the findings:

{findings}

Top sources:
{top_sources}

Create a comprehensive answer that:
1. States clearly whether this is CONFIRMED WORKING or EXPERIMENTAL/THEORETICAL
2. Provides step-by-step instructions if available
3. Notes any prerequisites or compatibility requirements
4. Warns about known issues or pitfalls
5. Suggests fallback approaches if the primary solution might fail
6. Cites which sources each claim comes from

If we couldn't find a confirmed solution, be HONEST about that and explain:
- What IS known
- What the most promising approach seems to be
- What specific risks or unknowns remain
- What the user should try first

NEVER fabricate steps. If a step is uncertain, say so explicitly."""


class SolutionSynthesizer:
    """Combines all findings into a final, actionable answer."""

    def __init__(self, llm: LLMProvider):
        self.llm = llm

    async def synthesize(self, plan: ResearchPlan) -> str:
        findings_text = "\n\n".join(
            f"Finding {i+1} (confidence: {f.confidence:.0%}, "
            f"verified: {f.is_verified}, "
            f"cross-verified: {f.cross_verified}):\n{f.claim}"
            for i, f in enumerate(plan.findings)
        )

        top_results = sorted(
            plan.all_results,
            key=lambda r: r.relevance_score,
            reverse=True,
        )[:10]

        top_sources_text = "\n".join(
            f"- [{r.source_tier.name}] {r.title} ({r.url})\n"
            f"  Relevance: {r.relevance_score:.0%} | "
            f"Working solution: {r.has_working_solution}\n"
            f"  Steps: {r.solution_steps[:3] if r.solution_steps else 'None'}"
            for r in top_results
        )

        return await self.llm.complete(
            system_prompt="You are a precise technical writer. Never fabricate information.",
            user_prompt=SYNTHESIZER_PROMPT.format(
                query=plan.original_query,
                num_sources=len(plan.all_results),
                findings=findings_text or "No high-confidence findings yet.",
                top_sources=top_sources_text or "No highly relevant sources found.",
            ),
            temperature=0.2,
        )


